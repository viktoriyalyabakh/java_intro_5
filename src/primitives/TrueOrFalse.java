package primitives;

public class TrueOrFalse {
    public static void main(String[] args) {
        /*
        boolean is used to store either true or false
        It will take only 1 bit
        */


        boolean b1 = true;
        boolean b2 = false;

        System.out.println(b1); // true
        System.out.println(b2); // false
    }
}
