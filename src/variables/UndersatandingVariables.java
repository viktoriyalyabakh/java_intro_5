package variables;

public class UndersatandingVariables {
    public static void main(String[] args) {

        //Variable declaration and assignment
        int age = 45;

        System.out.println(age);

        //Variable declaration
        String name = "John";

        System.out.println(name);

        //Re-assigning
        name = "Mike";
        System.out.println(name);

        age = 21;
        System.out.println(age);



    }
}
